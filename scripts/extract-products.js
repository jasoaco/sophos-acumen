#!/usr/bin/env node
// Extracts the PRODUCTS object from sophos-field-guide.html and writes data/products.js
// Usage: node scripts/extract-products.js path/to/sophos-field-guide.html

const fs = require("fs");
const path = require("path");

const htmlPath = process.argv[2];
if (!htmlPath) {
  console.error("Usage: node scripts/extract-products.js <path-to-sophos-field-guide.html>");
  process.exit(1);
}

const src = fs.readFileSync(htmlPath, "utf8");

// Find the PRODUCTS object — it starts with `const PRODUCTS = {` or `var PRODUCTS = {`
const startMatch = src.match(/(?:const|var|let)\s+PRODUCTS\s*=\s*\{/);
if (!startMatch) {
  console.error("Could not find PRODUCTS object in the file. Check the variable name.");
  process.exit(1);
}

const startIdx = startMatch.index + startMatch[0].length - 1; // position of opening {

// Walk forward to find the matching closing brace
let depth = 0;
let endIdx = startIdx;
for (let i = startIdx; i < src.length; i++) {
  if (src[i] === "{") depth++;
  else if (src[i] === "}") {
    depth--;
    if (depth === 0) { endIdx = i; break; }
  }
}

const productsLiteral = src.slice(startIdx, endIdx + 1);

const output = `// Auto-generated by scripts/extract-products.js — do not edit manually
// Source: ${path.basename(htmlPath)}
// Generated: ${new Date().toISOString()}

window.PRODUCTS = ${productsLiteral};
`;

const outPath = path.join(__dirname, "..", "data", "products.js");
fs.writeFileSync(outPath, output, "utf8");
console.log(`Wrote ${outPath}`);
